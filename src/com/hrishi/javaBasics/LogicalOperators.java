package com.hrishi.javaBasics;

public class LogicalOperators {
    public static void main(String[] args) {
        System.out.println("Logical Operators in Java");
        int x= 19;
        int y= 17;
        // AND Operator
        System.out.println(x>18 && y>18);

        // OR Operator
        System.out.println(x>18 || y>18);

        // NOT Operator
        System.out.println(!(y>18));

    }
}
