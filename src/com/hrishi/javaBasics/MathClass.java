package com.hrishi.javaBasics;

public class MathClass {
    public static void main(String[] args) {
        System.out.println("Math Class in Java");
        System.out.println(Math.ceil(7.18));
        System.out.println(Math.ceil(7.81));
        System.out.println(Math.max(2,5));
        System.out.println(Math.floor(7.18));
        System.out.println(Math.floor(7.81));
        System.out.println(Math.min(3,5));
        System.out.println(Math.random());
    }
}
