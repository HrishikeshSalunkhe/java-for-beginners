package com.hrishi.javaBasics;

public class ArithmeticExpression {
    public static void main(String[] args) {
        System.out.println("Arithmetic Expression");
        int num1=100;
        int num2=50;
        System.out.println(num1+num2);
        System.out.println(num1-num2);
        System.out.println(num1*num2);
        System.out.println(num1/num2);
        System.out.println(num1%num2);
    }
}
